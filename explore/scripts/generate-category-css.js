#!/usr/bin/env node
/**
 * Fetches category colour data from eolas and writes the generated CSS file
 * used by the explore UI.  Run automatically as the first step of `npm run build`.
 */

import { writeFileSync, mkdirSync, existsSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));

// In Docker, `COPY src .` flattens the src/ prefix — resources/ lands directly
// in WORKDIR.  Locally, resources/ lives under src/.  Detect which layout we're in.
const resourcesDir = existsSync(join(__dirname, '..', 'src', 'resources'))
	? join(__dirname, '..', 'src', 'resources')
	: join(__dirname, '..', 'resources');
const OUTPUT_FILE = join(resourcesDir, 'categories.css');
const EOLAS_URL = 'https://eolas.l42.eu/metadata/categories.json';

async function main() {
	console.log(`Fetching category colours from ${EOLAS_URL} ...`);
	const response = await fetch(EOLAS_URL);
	if (!response.ok) {
		throw new Error(`Failed to fetch category colours: HTTP ${response.status}`);
	}
	const categories = await response.json();

	const cssLines = [
		'/* Generated by scripts/generate-category-css.js — do not edit by hand */',
		'/* Run `npm run build` to regenerate from the eolas categories endpoint */',
		'',
	];

	for (const { name, background, border, text } of categories) {
		cssLines.push(`[data-category="${name}"] {`);
		cssLines.push(`\t--category-background: ${background};`);
		cssLines.push(`\t--category-border: ${border};`);
		cssLines.push(`\t--category-text: ${text};`);
		cssLines.push('}');
		cssLines.push('');
	}

	mkdirSync(dirname(OUTPUT_FILE), { recursive: true });
	writeFileSync(OUTPUT_FILE, cssLines.join('\n'));
	console.log(`Written ${OUTPUT_FILE} (${categories.length} categories)`);
}

main().catch(err => {
	console.error(`Error generating category CSS: ${err.message}`);
	process.exit(1);
});
